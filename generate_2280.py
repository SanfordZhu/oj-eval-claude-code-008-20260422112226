#!/usr/bin/env python3

# Generate mov code for problem 2280 (printf)
# Creates lookup tables for hundreds, tens, ones digits

def generate_mov_code():
    code = []
    code.append("# Problem 2280: printf")
    code.append("# Read a character, output its decimal ASCII code")
    code.append("# Generated by Python script")
    code.append("")

    # Read input character
    code.append("# Read input character")
    code.append("A<I")
    code.append("")

    # We'll use memory regions:
    # 1000-1255: hundreds digit (ASCII char or 0 for skip)
    # 2000-2255: tens digit (ASCII char or 0 for skip)
    # 3000-3255: ones digit (ASCII char)

    code.append("# Look up hundreds digit (0 for skip, ASCII digit otherwise)")
    code.append("B<[A+1000]")
    code.append("")
    code.append("# Look up tens digit (0 for skip, ASCII digit otherwise)")
    code.append("C<[A+2000]")
    code.append("")
    code.append("# Look up ones digit (always ASCII digit)")
    code.append("D<[A+3000]")
    code.append("")

    # Output logic: need to skip output if digit is 0 (meaning skip)
    # We'll use indirect output: O<[X] where X is 0 or digit address
    # Set up output table: address 4000 = 0 (null), address 4001..4010 = '0'..'9'

    # First, initialize output table
    code.append("# Initialize output table (0=null, 1='0', ..., 10='9')")
    code.append("[4000]<0")
    for i in range(10):
        code.append(f"[{4001+i}]<{48+i}")  # '0' to '9'

    # Convert B (0 or digit) to index: if B==0, index=0; else index=B-47
    # But we can't compute B-47... Instead, precompute mapping for all B values
    # We'll create another table: convert_table[B] gives index

    code.append("# Initialize convert table: maps digit char to output table index")
    code.append("# 0 -> 0, '0'(48) -> 1, '1'(49) -> 2, ..., '9'(57) -> 10")
    for i in range(256):
        if i == 0:
            value = 0
        elif 48 <= i <= 57:
            value = i - 47  # 48->1, 49->2, ..., 57->10
        else:
            value = 0  # shouldn't happen
        code.append(f"[{5000+i}]<{value}")

    # Now convert and output
    code.append("# Convert and output hundreds digit")
    code.append("E<[B+5000]")  # E = index (0 or 1..10)
    code.append("O<[E+4000]")  # output from table

    code.append("# Convert and output tens digit")
    code.append("E<[C+5000]")
    code.append("O<[E+4000]")

    code.append("# Convert and output ones digit")
    code.append("E<[D+5000]")
    code.append("O<[E+4000]")
    code.append("")
    code.append("# Halt")
    code.append("Z<1")
    code.append("")

    # Generate table initializations FIRST (before main code)
    tables = []
    tables.append("# Initialize lookup tables")

    # Hundreds table (address 1000-1255)
    for i in range(256):
        hundreds = i // 100
        if hundreds == 0:
            value = 0  # output nothing for leading zero
        else:
            value = 48 + hundreds  # ASCII digit
        tables.append(f"[{1000+i}]<{value}")

    # Tens table (address 2000-2255)
    for i in range(256):
        tens = (i % 100) // 10
        hundreds = i // 100
        if hundreds == 0 and tens == 0:
            value = 0  # skip leading zero
        else:
            value = 48 + tens
        tables.append(f"[{2000+i}]<{value}")

    # Ones table (address 3000-3255)
    for i in range(256):
        ones = i % 10
        value = 48 + ones
        tables.append(f"[{3000+i}]<{value}")

    # Combine: tables first, then main code
    return "\n".join(tables + [""] + code)

if __name__ == "__main__":
    mov_code = generate_mov_code()
    with open("/workspace/problem_008/code/2280.mv", "w") as f:
        f.write(mov_code)
    print("Generated 2280.mv")